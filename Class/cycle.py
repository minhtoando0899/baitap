# Write a Python class named Circle constructed by a radius and two methods which will compute the area and the perimeter of a circle.

class Cycle:
    def __init__(self, r):
        self.r = r

    def area(self):
        are = self.r * 2 * 3.14
        print(are)

    def per(self):
        pre = (self.r) ** 2 * 3.14
        print(pre)


C1 = Cycle(10)
C1.area()
C1.per()
